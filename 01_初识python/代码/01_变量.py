# 变量：可以发生改变的一个量，变量是用来区分不同数据的，指向一个内存空间, 并且帮我们存储一些数据。
"""
变量的命名规范：
  2.1.必须是数字或字母或下划线组成
  2.2.不能是数字开头，更不能是纯数字
  2.3.不能使用python的关键字
  2.4.不要用中文
  2.5.不要太长
  2.6.要有意义
  2.7.推荐使用下划线命名
"""

# 1.变量的表示方式如下：
# 1.1. 字母
a = 188
print(a)
a = 166
print(a)

# 1.2. 下划线
_ = 3

# 1.3. 数字或字母或下划线的组合
a3_ = 4

# 1.4. 不能是数字，将报错
# 3 = 6

# 1.5. 不用能中文，本地能使用但是其他国家。。。
# 樵夫 = "很帅"

# 1.6. 不能使用python的关键字，比如if/while/else
# if = 3

# 1.7.不要太长(不报错，只是规范)
zhongongngngnngnngngngngngngngngngnngngngngngnggngngnngngngngngngngngngngngngngngngngnng  = "ss"

# 1.8. 要有意义
qwer = "fs"

# 1.9. 推荐使用下划线命名或者驼峰命名
name_of_teacher = "樵夫" # 下划线
nameOfTeacher = "樵夫"   # 驼峰


